//Number
console.log(typeof 2);
console.log(typeof -356);
console.log(typeof 8.357);

//Ops aritméticas
console.log(2+4);
console.log(333-12);

//Special numbers
console.log(typeof Infinity);
console.log(typeof -Infinity);
console.log(typeof NaN);

//String
console.log("Cacareco")
console.log("135")
console.log("Um texto em \nduas linhas")
console.log("Espaço de \ttab")

//Template strings
//interpolação
console.log(`A soma de 2 + 2 é: ${2 + 2}`)

//Booleans
console.log(3 > 2)
console.log(55 === 12)

//empty values
console.log(typeof null, typeof undefined)

